#include "enemy.h"

Enemy::Enemy(int x, int y, int health, int damage, const std::string& type)
    : x(x), y(y), health(health), damage(damage), type(type) {}

int Enemy::getX() const {
    return x;
}

int Enemy::getY() const {
    return y;
}

int Enemy::getHealth() const {
    return health;
}

int Enemy::getDamage() const {
    return damage;
}

std::string Enemy::getType() const {
    return type;
}

void Enemy::move(char direction) {
    switch (direction) {
        case 'w': --y; break;
        case 's': ++y; break;
        case 'a': --x; break;
        case 'd': ++x; break;
    }
}

void Enemy::takeDamage(int damage) {
    health -= damage;
    if (health < 0) {
        health = 0;
    }
}

void Enemy::increaseStrength(int healthIncrement, int damageIncrement) {
    health += healthIncrement;
    damage += damageIncrement;
}

Ghost::Ghost(int x, int y, int health, int damage) : Enemy(x, y, health, damage, "Ghost") {}

